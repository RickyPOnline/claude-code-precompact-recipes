#!/usr/bin/env bash
# 03-handoff-memo.sh · PreCompact recipe
#
# Generates a "what's in flight" handoff note before compaction so the
# next session reads it on resume instead of trying to reconstruct
# context from the harness's generic summary.
#
# Two strategies offered — pick one and uncomment:
#   A. Cheap LLM summary (requires API key + curl/jq) — best memo quality
#   B. Tail of recent assistant turns — no LLM call, zero cost
#
# Output lands at ~/.claude/handoffs/handoff-<timestamp>.md
# A companion SessionStart hook can cat the newest file into the next session.
#
# Exits 0 always.
set -u

LOG="$HOME/.claude/hooks/precompact-handoff.log"
HANDOFF_DIR="$HOME/.claude/handoffs"
mkdir -p "$(dirname "$LOG")" "$HANDOFF_DIR"

HOOK_JSON=$(cat 2>/dev/null || echo '{}')
TS=$(date -u '+%Y-%m-%d %H:%M:%S')
STAMP=$(date -u +%Y%m%d-%H%M%S)
MEMO="$HANDOFF_DIR/handoff-$STAMP.md"

echo "[$TS] FIRED" >> "$LOG"

TRANSCRIPT=$(echo "$HOOK_JSON" | python3 -c "
import sys, json
try:
    d = json.loads(sys.stdin.read() or '{}')
    print(d.get('transcript_path', ''))
except Exception:
    pass
" 2>/dev/null)

if [ -z "$TRANSCRIPT" ] || [ ! -f "$TRANSCRIPT" ]; then
  echo "[$TS] NO_TRANSCRIPT" >> "$LOG"
  exit 0
fi

# ── Strategy B (zero-cost) · tail of recent assistant text ───────────────
# This pulls the last ~5 assistant turns as raw text. Cheap, deterministic.
{
  echo "# Pre-compact handoff · $TS"
  echo ""
  echo "Generated by PreCompact hook before context was summarized."
  echo "Session: $(basename "$TRANSCRIPT" .jsonl)"
  echo ""
  echo "## Last 5 assistant turns (most recent first)"
  echo ""
  python3 - "$TRANSCRIPT" <<'PY'
import sys, json
path = sys.argv[1]
turns = []
with open(path, 'r') as f:
    for line in f:
        try:
            msg = json.loads(line)
        except Exception:
            continue
        if msg.get('type') != 'assistant':
            continue
        content = msg.get('message', {}).get('content', [])
        if not isinstance(content, list):
            continue
        text = '\n'.join(c.get('text', '') for c in content
                         if isinstance(c, dict) and c.get('type') == 'text')
        text = text.strip()
        if text:
            turns.append(text)
for i, t in enumerate(reversed(turns[-5:]), 1):
    print(f"### Turn -{i}\n")
    print(t[:2000])
    print("\n---\n")
PY
} > "$MEMO" 2>>"$LOG"

echo "[$TS] WROTE · $MEMO" >> "$LOG"

# ── Strategy A (LLM-powered) · uncomment if you have an API key ──────────
